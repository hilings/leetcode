class Solution {
public:
    vector<int> findMinHeightTrees0(int n, vector<pair<int, int>>& edges) {	// brute force
        vector<int> ret;
        vector<vector<int>> vEdges (n, vector<int> {});
        for (pair<int,int> e: edges) {
            vEdges[e.first].push_back(e.second);
            vEdges[e.second].push_back(e.first);
        }

        cout << "edges:\n";
        for (int i = 0; i < n; i++) {
            cout << i << ": ";
            for (int a: vEdges[i]) {
                cout << a << ' ';
            }
            cout << '\n';
        }

        vector<vector<int>> vMinHeightRoots (n, vector<int> {});
        int minHeight = n;

        for (int i = 0; i < n; i++) {
            cout << "\nroot: " << i << '\n';
            vector<int> vDepths (n, 0);
            vector<int> vNext {i};
            int height = 0;
            for (int k = 0; k < vNext.size(); k++) {
                for (int e: vEdges[vNext[k]]) {
                    if (e == i || vDepths[e] > 0)
                        continue;
                    vDepths[e] = vDepths[vNext[k]]+1;
                    vNext.push_back(e);
                    if (vDepths[e] > height)
                        height = vDepths[e];
                }
            }
            cout << "vDepths:\n";
            for (int j = 0; j < n; j++) {
                cout << j << ' ' << vDepths[j] << '\n';
            }
            cout << "height: " << height << '\n';

            vMinHeightRoots[height].push_back(i);

            if (height < minHeight)
                minHeight = height;

        }

        return vMinHeightRoots[minHeight];
    }

    vector<int> findMinHeightTrees(int n, vector<pair<int, int>>& edges) {
        if (n == 1)
            return vector<int> {0};
        vector<unordered_set<int>> vNeighbors (n, unordered_set<int> {});
        for (pair<int,int> p: edges) {
            vNeighbors[p.first].insert(p.second);
            vNeighbors[p.second].insert(p.first);
        }

        /*
         for (int i = 0; i < n; i++) {
         cout << i << ": ";
         for (int a: vNeighbors[i]) {
         cout << a << ' ';
         }
         cout << '\n';
         }
         cout << '\n';
         */

        unordered_set<int> todo;
        for (int i = 0; i < n; i++) {
            if (vNeighbors[i].size() == 1)
                todo.insert(i);
        }

        int notVisited = n;
        while (notVisited > 2) {
            unordered_set<int> nextRound;
            for (int cur: todo) {
                for (int neighbor: vNeighbors[cur]) {
                    vNeighbors[neighbor].erase(cur);
                    if (vNeighbors[neighbor].size() == 1)
                        nextRound.insert(neighbor);
                }
            }
            notVisited -= todo.size();
            todo.swap(nextRound);
        }

        vector<int> ret (todo.begin(), todo.end());
        return ret;
    }

    vector<int> findMinHeightTrees2(int n, vector<pair<int, int>>& edges) {
        if (n == 1)
            return vector<int> {0};
        vector<int> degrees (n, 0);
        vector<vector<int>> vNeighbors (n, vector<int> {});
        for (pair<int,int> p: edges) {
            vNeighbors[p.first].push_back(p.second);
            vNeighbors[p.second].push_back(p.first);
            degrees[p.first]++;
            degrees[p.second]++;
        }

        /*
         for (int i = 0; i < n; i++) {
         cout << i << ": ";
         for (int a: vNeighbors[i]) {
         cout << a << ' ';
         }
         cout << '\n';
         }
         cout << '\n';
         */

        vector<int> vLeaf;
        for (int i = 0; i < n; i++) {
            if (vNeighbors[i].size() == 1)
                vLeaf.push_back(i);
        }
        int notVisited = n;
        while (notVisited > 2) {
            vector<int> nextRound;
            for (int cur: vLeaf) {
                for (int neighbor: vNeighbors[cur]) {
                    if (--degrees[neighbor] == 1)
                        nextRound.push_back(neighbor);
                }
            }
            notVisited -= vLeaf.size();
            vLeaf.swap(nextRound);
        }
        return vLeaf;
    }
};

